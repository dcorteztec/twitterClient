package br.com.twitterClient.helpVO;

public class TweetsStringHelperVO {

	private String maisComum;
	private String maisLonga;
	private Long qtd;
	private Integer tamanho;
	
	public String getMaisComum() {
		return maisComum;
	}
	public void setMaisComum(String maisComum) {
		this.maisComum = maisComum;
	}
	public String getMaisLonga() {
		return maisLonga;
	}
	public void setMaisLonga(String maisLonga) {
		this.maisLonga = maisLonga;
	}
	public Long getQtd() {
		return qtd;
	}
	public void setQtd(Long qtd) {
		this.qtd = qtd;
	}
	public Integer getTamanho() {
		return tamanho;
	}
	public void setTamanho(Integer tamanho) {
		this.tamanho = tamanho;
	}
	
	
}
